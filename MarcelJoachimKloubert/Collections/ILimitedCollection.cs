namespace MarcelJoachimKloubert.Collections
{
    /// <summary>
    /// Describes a collection with a limited size.
    /// </summary>
    public interface ILimitedCollection
    {
        #region Properties

        /// <summary>
        /// Gets the number of items of that collection.
        /// </summary>
        int Count { get; }

        /// <summary>
        /// Gets the maximum number of possible items.
        /// </summary>
        int MaxCount { get; }

        /// <summary>
        /// Gets if an exception is thrown if more than the supported maximum size of items
        /// is trying to be added or not.
        /// </summary>
        bool ThrowOnOverflow { get; }

        #endregion Properties
    }
}